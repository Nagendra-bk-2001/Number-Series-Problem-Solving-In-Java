class SwapNumberUsingThree
{
	public static void main(String[] args)
	{
		int a=10;
		int b=20;
		int temp=0;
		
		temp=a;
		a=b;	//Using Addition and Subtraction 
		b=temp;

		System.out.print("The is an  " + a + " And " + b + " IS Swap Number");
	}
}

/*
The is an  20 And 10 IS Swap Number
*/
